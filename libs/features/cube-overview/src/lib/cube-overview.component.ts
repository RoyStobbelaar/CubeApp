import { Component, OnInit } from '@angular/core';

// Interface of CARD
export type MTGColorType = 'Black' | 'Red' | 'White' | 'Blue' | 'Green';

// NOTE: Graveyard are cards that actually fill the graveyard for delve cards or casteable cards from graveyard
// NOTE: Blink also means enters the battlefield effects, as you prolly want blink effects on your FTK or Chupacabra anyway
// NOTE: Ramp are cards that ramp, and payoffs or Xspells (Eldrazi's, Blaze, etc.)
export type MTGArchType =
  | 'Sacrifice'
  | 'Spells'
  | 'Counters'
  | 'Graveyard'
  | 'Lifegain'
  | 'Reanimate'
  | 'Ramp'
  | 'Tokens'
  | 'Blink';
export type MTGCardType = 'Creature' | 'Spell' | 'Land' | 'Other'; // Artifact / Ench / PW

export interface MagicCube {
  name: string;
  image: string;
  cards: MagicCard[];
}
export interface MagicCard {
  name: string;
  url: string;
  cmc: number; //Total mana cost
  type: MTGCardType;
  colors: MTGColorType[]; // Can be multiple, just for stats basically
  archType: MTGArchType[]; // Can also be multiple (sac/gy do together often)
  // Stuff like attack/defense stats are not important for stats
}

